package com.algorithm.model;

public class Pair {
    int a; int b;

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public Pair(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
